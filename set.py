# Sets are similar to lists or dictionaries. 

# They are created using curly braces, and are unordered, 
# which means that they can't be indexed. 

# Due to the way they're stored, it's faster to check whether an 
# item is part of a set using the in operator, rather than part of a list.

num_set = {1,2,3,4,5}

print(1 in num_set)